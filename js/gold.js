// gold.js

////////////////////////////////////////////////////////
// INITIALIZE GOLD
////////////////////////////////////////////////////////

export function initializeGold(playerStats, startingGold = 20) {
    playerStats.gold = Math.max(0, startingGold);
}

////////////////////////////////////////////////////////
// GET GOLD
////////////////////////////////////////////////////////

export function getGold(playerStats) {
    return playerStats.gold ?? 0;
}

////////////////////////////////////////////////////////
// ADD GOLD
////////////////////////////////////////////////////////

export function addGold(playerStats, amount) {
    if (amount <= 0) return;

    playerStats.gold = (playerStats.gold ?? 0) + amount;
}

////////////////////////////////////////////////////////
// REMOVE GOLD
////////////////////////////////////////////////////////

export function removeGold(playerStats, amount) {
    if (amount <= 0) return false;

    const current = playerStats.gold ?? 0;

    if (current < amount) {
        return false; // not enough gold
    }

    playerStats.gold = current - amount;
    return true;
}

////////////////////////////////////////////////////////
// SET GOLD (SAFE)
////////////////////////////////////////////////////////

export function setGold(playerStats, amount) {
    playerStats.gold = Math.max(0, amount);
}